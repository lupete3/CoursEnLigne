				<h4>6. MODALITES POUR LES INSCRIPTIONS A L'INSTITUT D'IBANDA</h4><br>
						
						<p> Pour prendre inscription à l'institut d'Ibanda,il faut obligatoirement procéder de la manière suvante:</p>
<ul>
                                <li>  • Avoir un compte sur le site de l'IIB : grace à ce ceci,vous avez l'occasion d'etre informé à temps et à contre temps sur ce qui se passe à l'institut d'Ibanda,et surtout en ce qui concerne<br>les inscriptions des nouveaux élèves;</li>
                                <li>  • Se connecter à son compte pour solliciter l'inscription;</li>
								<li>  • Sur le formulaire d'inscription qui s'affiche,compléter la vraie et complète identité.En suite;</li>
                                <li>  • Joindre son dossier complet (Zippé ou compressé) :ce dossier doit être constitué des éléments tels que le bulletin de la classe précedente (ce bulletin doit être accompagné d'un journal<br>publié de l'ENAFEP pour attester la réussite en 6eme année primaire au cas où on sollicite l'inscription en 1ere CO);le réçu scanné pour paiement de frais d'inscriptions à la caisse de l'IIB ou le bordereau scanné de paiement effectué au compte de l'école à la TMB.</li>
								<li>  • Pour terminer,il faut envoyer la demande.</li>
</ul>
						<p> Tout ceci doit être accompagé d'une lettre de demande d'inscription</p><br>
						<p>N.B: Apres avoir vérifier la véracité et l'exactitude du dossier,l'on poura vous notifier si votre demande a été acceptée ou rejettée;cela peut prendre même deux jours.</p><br>
