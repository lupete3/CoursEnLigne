				<h4>1. HISTORIQUE</h4><br>
                    
                     <P style="margin-right:25px";>
A l’époque du Congo belge, Vers 1946, GODDING, le ministre Belge du parti libéral a accordé les missions étrangères de créer des athénées royaux pour les enfants blancs. Ainsi s’annonce une réglementation de 1948 qui visait l’amélioration de l’enseignement primaire, le développement de l’enseignement secondaire et la création de l’enseignement supérieur et universitaire.
         En 1954, le ministre de colonies belges AUGUSTE BUISSERT a instauré l’enseignement officiel pour les enfants congolais.Ce sont les Athénées interraciaux ou groupes scolaires appelés Ecoles laïcs. L’institut d’IBANDA jadis athénée Royal était l’initiative du gouvernement à l’époque du Congo Belge

